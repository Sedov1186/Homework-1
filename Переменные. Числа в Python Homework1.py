a = int(input('''Перводим:
1 - градусы в радианы,
2 - радианы в градусы:
>>'''))

if a == 1:
    b = float(input('Введите градусы '))
    result = b * 0.017453
    print(b, 'градусах = ', result,' в радианах.')

elif a == 2:
    b = float(input('Введите радианы '))
    result = b / 0.017453
    print(b, 'в радианах  = ', result, ' в градусах.')